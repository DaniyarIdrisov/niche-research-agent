"""
Рендер docs/REPORT.md в одностраничный PDF через цепочку:
  markdown.markdown(...)  →  REPORT.html  →  msedge --headless --print-to-pdf  →  REPORT.pdf

Зачем такой путь:
- pandoc + LaTeX (xelatex) — самый «академический» рендер, но требует LaTeX-дистрибутива (~3 ГБ).
- weasyprint / wkhtmltopdf — отдельная установка, нестабильны на Windows.
- Edge / Chrome headless — есть на каждой Windows-машине из коробки, поддерживает CSS print, кириллицу,
  embedding изображений по относительным путям из file:// URL. Один вызов, без зависимостей.

Запуск:
    python scripts/render_report_pdf.py

Результат: docs/REPORT.html + docs/REPORT.pdf
"""

