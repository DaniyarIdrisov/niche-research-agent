def md_to_html(md_text: str) -> tuple[str, dict]:
    # Парсим YAML-frontmatter в meta для титульного блока
    meta: dict[str, str] = {}
    m = re.match(r"^---\n(.*?)\n---\n", md_text, flags=re.DOTALL)
    if m:
        for line in m.group(1).splitlines():
            if ":" in line:
                k, _, v = line.partition(":")
                meta[k.strip()] = v.strip().strip('"').strip("'")
        md_text = md_text[m.end():]

    # Заменяем pandoc-LaTeX директивы на HTML-эквиваленты
    md_text = md_text.replace(
        r"\newpage", '<div style="page-break-after: always;"></div>'
    )
    md_text = md_text.replace(r"\tableofcontents", "[TOC]")

    md = markdown.Markdown(
        extensions=[
            "extra",       # tables, fenced_code, footnotes, attr_list, def_list, etc.
            "toc",         # auto-TOC при [TOC]
            "sane_lists",
            "codehilite",  # подсветка кода через pygments
        ],
        extension_configs={
            "toc": {"title": "Содержание", "permalink": False},
            "codehilite": {"guess_lang": False, "use_pygments": True},
        },
    )

    body = md.convert(md_text)
    return body, meta


def build_html(body: str, meta: dict) -> str:
    title = meta.get("title", "Отчёт")
    subtitle = meta.get("subtitle", "")
    author = meta.get("author", "")
    date = meta.get("date", "")

    title_block = f"""
<div class="title-block">
  <div class="title">{title}</div>
  <div class="subtitle">{subtitle}</div>
  <div class="author">{author}</div>
  <div class="date">{date}</div>
</div>
"""

